public class Album {

    private int id;
    private String name;
    private int artisId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getArtisId() {
        return artisId;
    }

    public void setArtisId(int artisId) {
        this.artisId = artisId;
    }
}
